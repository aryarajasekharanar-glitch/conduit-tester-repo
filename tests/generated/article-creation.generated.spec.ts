import { test, expect } from '@playwright/test';

const BACKEND_URL = process.env.BACKEND_URL || 'http://localhost:3000/api';

test.describe('POST /articles - createArticle', () => {
  let authToken: string;
  let userId: number;

  test.beforeEach(async ({ request }) => {
    const timestamp = Date.now();
    const registerResponse = await request.post(`${BACKEND_URL}/users`, {
      data: {
        user: {
          username: `testuser${timestamp}`,
          email: `testuser${timestamp}@example.com`,
          password: 'password123',
        },
      },
    });
    const registerData = await registerResponse.json();
    authToken = registerData.user.token;
    userId = registerData.user.id;
  });

  test('should create article successfully with all required fields and return slug with userId suffix', async ({ request }) => {
    const articleData = {
      article: {
        title: 'Test Article Title',
        description: 'Test description',
        body: 'Test body content',
        tagList: ['tag1', 'tag2'],
      },
    };

    const response = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });

    expect(response.status()).toBe(201);
    const data = await response.json();
    expect(data.article).toBeDefined();
    expect(data.article.title).toBe('Test Article Title');
    expect(data.article.description).toBe('Test description');
    expect(data.article.body).toBe('Test body content');
    // NOTE: this assertion was generated by the agent and is WRONG -- it
    // assumes slugify() lowercases the title. The real backend's slugify
    // is case-preserving (confirmed by running this test against the live
    // server: actual slug is "Test-Article-Title-<id>", not lowercase).
    // Left failing intentionally as documented evidence of the agent
    // hallucinating third-party library behavior it had no visibility into.
    // See writeup for how this informs the "keep output honest" answer.
    expect(data.article.slug).toBe(`Test-Article-Title-${userId}`); // corrected
    expect(data.article.tagList).toEqual(['tag1', 'tag2']);
  });

  test('should return 422 with specific error when title is missing', async ({ request }) => {
    const articleData = {
      article: {
        description: 'Test description',
        body: 'Test body content',
      },
    };

    const response = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });

    expect(response.status()).toBe(422);
    const data = await response.json();
    expect(data.errors.title).toEqual(["can't be blank"]);
  });

  test('should return 422 with specific error when description is missing', async ({ request }) => {
    const articleData = {
      article: {
        title: 'Test Article',
        body: 'Test body content',
      },
    };

    const response = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });

    expect(response.status()).toBe(422);
    const data = await response.json();
    expect(data.errors.description).toEqual(["can't be blank"]);
  });

  test('should return 422 with specific error when body is missing', async ({ request }) => {
    const articleData = {
      article: {
        title: 'Test Article',
        description: 'Test description',
      },
    };

    const response = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });

    expect(response.status()).toBe(422);
    const data = await response.json();
    expect(data.errors.body).toEqual(["can't be blank"]);
  });

  test('should return 422 with must be unique error when creating article with same title for same user', async ({ request }) => {
    const articleData = {
      article: {
        title: 'Duplicate Title Test',
        description: 'First description',
        body: 'First body',
      },
    };

    const firstResponse = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });
    expect(firstResponse.status()).toBe(201);

    const secondResponse = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });

    expect(secondResponse.status()).toBe(422);
    const data = await secondResponse.json();
    expect(data.errors.title).toEqual(['must be unique']);
  });

  test('should create article with empty tagList when tagList is not provided', async ({ request }) => {
    const articleData = {
      article: {
        title: 'No Tags Article',
        description: 'Description without tags',
        body: 'Body without tags',
      },
    };

    const response = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });

    expect(response.status()).toBe(201);
    const data = await response.json();
    expect(data.article.tagList).toEqual([]);
  });

  test('should create article with empty tagList when tagList is not an array', async ({ request }) => {
    const articleData = {
      article: {
        title: 'Non-Array Tags Article',
        description: 'Description with non-array tags',
        body: 'Body with non-array tags',
        tagList: 'not-an-array',
      },
    };

    const response = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });

    expect(response.status()).toBe(201);
    const data = await response.json();
    expect(data.article.tagList).toEqual([]);
  });

  test('should include author information in response', async ({ request }) => {
    const articleData = {
      article: {
        title: 'Article With Author',
        description: 'Testing author response',
        body: 'Body content',
      },
    };

    const response = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });

    expect(response.status()).toBe(201);
    const data = await response.json();
    expect(data.article.author).toBeDefined();
    expect(data.article.author.username).toBeDefined();
  });

  test('should initialize favoritesCount to 0 for new article', async ({ request }) => {
    const articleData = {
      article: {
        title: 'Favorites Count Test',
        description: 'Testing favorites count',
        body: 'Body content',
      },
    };

    const response = await request.post(`${BACKEND_URL}/articles`, {
      headers: {
        Authorization: `Token ${authToken}`,
      },
      data: articleData,
    });

    expect(response.status()).toBe(201);
    const data = await response.json();
    expect(data.article.favoritesCount).toBe(0);
  });
});